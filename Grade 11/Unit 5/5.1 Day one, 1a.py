# Description: A 5-element list initialized with asterisks.

# Process: Creates list.
list = ["*1", "*2", "*3", "*4", "*5"]

# Process: Prints the length of the list.
print(len(list))

# Process: Changes the value of the 3rd element.
list[3] = "#3"

# Output: Prints the 3rd and last elements of the list.
print(list[3])
print(list[-1])
